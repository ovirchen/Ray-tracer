#include "libft.h"

char			*ft_strcat(char *s1, const char *s2)
{
	size_t i;
	size_t d;

	i = 0;
	d = ft_strlen(s1);
	while (i < ft_strlen((char*)s2))
	{
		s1[d + i] = s2[i];
		i++;
	}
	s1[d + i] = '\0';
	return (s1);
}

#include <unistd.h>

void	ft_putendl_fd(char const *s, int fd)
{
	if (s)
	{
		while (*s && fd != -1)
		{
			write(fd, &s[0], 1);
			s++;
		}
		write(fd, "\n", 1);
	}
}

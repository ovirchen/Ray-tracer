#include "libft.h"

char	*ft_strdup(char *src)
{
	char	*str;
	size_t	i;

	if (src == NULL)
		return (NULL);
	i = ft_strlen(src);
	str = (char*)malloc(sizeof(char) * (i + 1));
	i = 0;
	while (src[i])
	{
		str[i] = src[i];
		i++;
	}
	str[i] = '\0';
	return (str);
}
